package JKad25;

public class JKad25D {
    public static void main(String[] args) {
        char a='A';
        char b='B';
        char c='C';

        System.out.print(a+"\t"+b+"\t"+c);
        System.out.println();
        System.out.print((int)a+"\t"+(int)b+"\t"+(int)c);
        System.out.println("インクリメントして表示します！");
        a++;
        b++;
        c++;
        System.out.print(a+"\t"+b+"\t"+c);
        System.out.println();
        System.out.print((int)a+"\t"+(int)b+"\t"+(int)c);

    }
}
